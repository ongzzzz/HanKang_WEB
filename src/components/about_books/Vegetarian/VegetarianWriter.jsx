const VegetarianWriter = () => {
  const {lang} = useTranslateStore();
  const [rootEl, setRootEl] = useState(null);
  const scrollRef = useRef(null);

  useEffect(() => {
    if (scrollRef.current) {
      setRootEl(scrollRef.current);
    }
  }, []);

  return (
    <div className="writer-container">
      <div className="writer-title">
        <p>2024 Nobel Prize in Literature Winner</p>
        <span className={`veget lang-${lang}`}>{lang === "kr" ? "채식주의자" : "素食者"}</span>
      </div>
      <div className={`writer-content lang-${lang}`} ref={scrollRef}>
        {(lang === "kr" ? textMapKR : textMapCN).map((text, idx) => (
          <motion.p
            key={idx}
            initial={{ opacity: 0, y: 50 }}
            whileInView={{ opacity: 1, y: 0}}
            viewport={{ 
              once: false,
              amount: 0.4,
              root: rootEl,
            }}
            transition={{
              ease: "easeInOut",
              duration: 0.8,
              delay: idx * 0.05,
            }}
          >
            {text}
          </motion.p>
        ))}
      </div>
    </div>
  );
};

export default VegetarianWriter;